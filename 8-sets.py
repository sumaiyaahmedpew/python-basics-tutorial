# Creating a set of numbers
numbers = {1, 2, 3, 4, 5}

# Adding a new number
numbers.add(6)

# Removing a number
numbers.remove(3)

# Duplicate values are automatically removed
numbers.add(2)

# Print all elements
print("Set of numbers:", numbers)

# Loop through the set
print("All numbers:")
for number in numbers:
    print(number)
